/**
 * CLASE VEHICULO
 */
class Vehicle {
    constructor(numberPlate, type, model, mark, year, color, seatsNum, costPerDay, image, available) {
        this.numberPlate = numberPlate;
        this.type = type;
        this.model = model;
        this.mark = mark;
        this.year = year;
        this.color = color;
        this.seatsNum = seatsNum;
        this.costPerDay = costPerDay;
        this.image = image;
        this.available = available;
    }

}